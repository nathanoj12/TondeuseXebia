public class Gazon {
    private Integer absMax;
    private Integer ordMax;

    public Gazon(Integer absMax, Integer ordMax) {
        this.absMax = absMax;
        this.ordMax = ordMax;
    }

    public Integer getAbsMax() {
        return absMax;
    }

    public void setAbsMax(Integer absMax) {
        this.absMax = absMax;
    }

    public Integer getOrdMax() {
        return ordMax;
    }

    public void setOrdMax(Integer ordMax) {
        this.ordMax = ordMax;
    }
}
